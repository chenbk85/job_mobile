#ifndef __PACKET_H__
#define __PACKET_H__

//////////////////////////////////////////////////////////////////////
//
#define YD_ZERO_MONITOR_ECHO    0x01A9 // 425
//
#define FILLER                  128

//////////////////////////////////////////////////////////////////////
// 바이트패킹
//
#pragma pack(1)  

//////////////////////////////////////////////////////////////////////
// HEADER
struct PACKET_HEARDER
{
	WORD Type;
	WORD Size;
};
//////////////////////////////////////////////////////////////////////
// BODY
// Echo Check
struct PACKET_MONITORCHO : PACKET_HEARDER
{
	ULONG		ulEchoCode;
	char		szEchoMsg[FILLER];

	PACKET_MONITORCHO()
	{
		Type = YD_ZERO_MONITOR_ECHO;
		Size = sizeof(*this);
		ulEchoCode	     = 0;
		ZeroMemory( szEchoMsg, sizeof(szEchoMsg) );
	}
};
//////////////////////////////////////////////////////////////////////
//
#pragma pack()
//////////////////////////////////////////////////////////////////////
// END
#endif  //__PACKET_H__



